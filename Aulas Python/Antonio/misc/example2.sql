use antonio_1;
CREATE TABLE POST(
Num INT PRIMARY KEY AUTO_INCREMENT,
Author INT NOT NULL,
Creation DATETIME NOT NULL,
Content TEXT NOT NULL,
CONSTRAINT fk_post_user2 FOREIGN KEY(Author) REFERENCES user2(num)
);

